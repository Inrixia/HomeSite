					<h4>UNIT OF CURRENT :</h4>
						The charge on an electron is measured in terms of coulomb. The unit of current is coulomb per second and is called ampere.Thus<br><br>
						I (Ampere) = coulomb/second = &xutri; q / &xutri; t<br>
						One coulomb is equivalent to the charge of 6.28 x 1018 electrons.<br><br>
