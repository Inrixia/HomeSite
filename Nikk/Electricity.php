					<h4>ELECTROMOTIVE FORCE :</h4>
						Electromotive force or potential of a body is the work done in joules to bring a unit electric charge from infinity to the body. It is expressed in terms of <b>volts (V)</b>.<br><br>
						The <b>potential difference</b> is defined as that which causes current to flow in the closed circuit.<br><br>
					<h4>RESISTANCE :</h4>
						Resistance is the property of a substance due to which it opposes the flow of electrons (i.e., electric current) through it. The unit of resistance is ohm (&Omega;).<br><br>
						Metals, acids and salt solutions are good conductors of electricity. Silver, copper and aluminium offer least resistance to flow of current and are called very good conductor of electricity. The electrons while flowing through the molecules or the atoms of the conductor, collide with other atoms and electrons, thereby producing heat.<br><br>
						Some substances offer relatively greater difficulty or hindrance to the passage of these electrons. Such substances are called poor conductors or insulators of electricity. Some of the insulators are glass, bakelite, mica, rubber, polyvinyl chloride (P.V.C.), dry wood, etc.<br><br>
					<h4>The resistance of a conductor depends on:</h4>
					<ul class="mainlists">
						<li>Length of conductor- it varies directly with the length</li>
						<li>Cross-sectional area of the conductor - it varies inversely with the cross-sectional area</li>
						<li>Its resistivity i.e.the nature of composition, etc., of the material of which the conductor is made up</li>
						<li>Temperature of the conductor - it almost varies directly with the temperature. Thus R, the resistance of a conductor is given by</li>
					</ul><br><br>
		 			<b>R = &rho; l / A</b><br><br>
					<b>where</b><br>    <!--This single sentance is quite important personally because I messed up and the bold tag wasn't closed. This compleately messed my whole website up. Tip for everyone, ALWAYS CLOSE THE TAGES-->
