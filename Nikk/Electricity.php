				<h2>Electricity at its basics</h2>
					Electricity is the set of physical phenomena associated with the presence and flow of electric charge. Electricity gives a wide variety of well-known effects, 
					such as lightning, static electricity, electromagnetic induction and electric current. In addition, electricity permits the creation and reception of electromagnetic radiation such as radio waves.<br><br>
					In electricity, charges produce electromagnetic fields which act on other charges. Electricity occurs due to several types of physics:<br>
					<ul class="mainlists">
						<li>electric charge: a property of some subatomic particles, which determines their electromagnetic interactions. Electrically charged matter is influenced by, and produces, 
						electromagnetic fields, electric charges can be positive or negative.</li>
						<li>electric field (see electrostatics): charges are surrounded by an electric field. The electric field produces a force on other charges.
						Changes in the electric field travel at the speed of light.</li>
						<li>electric potential: the capacity of an electric field to do work on an electric charge, typically measured in volts.</li>
						<li>electric current: a movement or flow of electrically charged particles, typically measured in amperes.</li>
						<li>electromagnets: Moving charges produce a magnetic field. Electric currents generate magnetic fields, and changing magnetic fields generate electric currents.</li>
					</ul>
					In electrical engineering, electricity is used for:<br>
					<ul class="mainlists">
						<li>electric power where electric current is used to energise equipment;</li>
						<li>electronics which deals with electrical circuits that involve active electrical components such as vacuum tubes, transistors, diodes and integrated circuits,
						and associated passive interconnection technologies.</li>
					</ul>
					Electrical phenomena have been studied since antiquity, though progress in theoretical understanding remained slow until the seventeenth and eighteenth centuries. 
					Even then, practical applications for electricity were few, and it would not be until the late nineteenth century that engineers were able to put it to industrial and residential use. 
					The rapid expansion in electrical technology at this time transformed industry and society. 
					Electricity's extraordinary versatility means it can be put to an almost limitless set of applications which include transport, heating, lighting, communications, and computation.
					Electrical power is now the backbone of modern industrial society.
				
				<br><h2>Concepts</h2>
					<h4>Electric charge</h4>
					A clear glass dome has an external electrode which connects through the glass to a pair of gold leaves. 
					<br><img class="mainimg2" src="http://i.imgur.com/gGmRnBE.png" alt="A charged rod touches the external electrode and makes the leaves repel."/><br>
					The presence of charge gives rise to an electrostatic force: charges exert a force on each other, an effect that was known, though not understood, in antiquity. 
					A lightweight ball suspended from a string can be charged by touching it with a glass rod that has itself been charged by rubbing with a cloth. 
					If a similar ball is charged by the same glass rod, it is found to repel the first: the charge acts to force the two balls apart. 
					Two balls that are charged with a rubbed amber rod also repel each other. However, if one ball is charged by the glass rod, and the other by an amber rod, the two balls are found to attract each other.
					These phenomena were investigated in the late eighteenth century by Charles-Augustin de Coulomb, who deduced that charge manifests itself in two opposing forms. 
					This discovery led to the well-known axiom: like-charged objects repel and opposite-charged objects attract.<br><br>
					The force acts on the charged particles themselves, hence charge has a tendency to spread itself as evenly as possible over a conducting surface. 
					The magnitude of the electromagnetic force, whether attractive or repulsive, is given by Coulomb's law, which relates the force to the product of the charges and has an inverse-square relation to the distance between them.
					The electromagnetic force is very strong, second only in strength to the strong interaction, but unlike that force it operates over all distances. 
					In comparison with the much weaker gravitational force, the electromagnetic force pushing two electrons apart is 1042 times that of the gravitational attraction pulling them together.<br><br>
					Study has shown that the origin of charge is from certain types of subatomic particles which have the property of electric charge. 
					Electric charge gives rise to and interacts with the electromagnetic force, one of the four fundamental forces of nature. 
					The most familiar carriers of electrical charge are the electron and proton. 
					Experiment has shown charge to be a conserved quantity, that is, the net charge within an isolated system will always remain constant regardless of any changes taking place within that system.
					Within the system, charge may be transferred between bodies, either by direct contact, or by passing along a conducting material, such as a wire.
					The informal term static electricity refers to the net presence (or 'imbalance') of charge on a body, usually caused when dissimilar materials are rubbed together, transferring charge from one to the other.<br><br>
					The charge on electrons and protons is opposite in sign, hence an amount of charge may be expressed as being either negative or positive. 
					By convention, the charge carried by electrons is deemed negative, and that by protons positive, a custom that originated with the work of Benjamin Franklin.
					The amount of charge is usually given the symbol Q and expressed in coulombs; each electron carries the same charge of approximately -1.6022x10-19 coulomb. 
					The proton has a charge that is equal and opposite, and thus +1.6022x10-19  coulomb. Charge is possessed not just by matter, but also by antimatter, each antiparticle bearing an equal and opposite charge to its corresponding particle.<br><br>
					Charge can be measured by a number of means, an early instrument being the gold-leaf electroscope, which although still in use for classroom demonstrations, has been superseded by the electronic electrometer.<br><br><br>
				
				<br><h4>Electrical Current</h4>
					The movement of electric charge is known as an electric current, the intensity of which is usually measured in amperes. 
					Current can consist of any moving charged particles; most commonly these are electrons, but any charge in motion constitutes a current.<br><br>
					By historical convention, a positive current is defined as having the same direction of flow as any positive charge it contains, or to flow from the most positive part of a circuit to the most negative part. 
					Current defined in this manner is called conventional current. 
					The motion of negatively charged electrons around an electric circuit, one of the most familiar forms of current, is thus deemed positive in the opposite direction to that of the electrons.
					However, depending on the conditions, an electric current can consist of a flow of charged particles in either direction, or even in both directions at once. 
					The positive-to-negative convention is widely used to simplify this situation.<br>
					<img class="mainimg2" src="http://i.imgur.com/U03i6bL.jpg" alt="The process by which electric current passes through a material is termed electrical conduction, and its nature varies with that of the charged particles and the material through which they are travelling. "/><br>
					
					Examples of electric currents include metallic conduction, where electrons flow through a conductor such as metal, and electrolysis, where ions (charged atoms) flow through liquids, 
					or through plasmas such as electrical sparks. 
					While the particles themselves can move quite slowly, sometimes with an average drift velocity only fractions of a millimetre per second,
					the electric field that drives them itself propagates at close to the speed of light, enabling electrical signals to pass rapidly along wires.<br><br>
					Current causes several observable effects, which historically were the means of recognising its presence. 
					That water could be decomposed by the current from a voltaic pile was discovered by Nicholson and Carlisle in 1800, a process now known as electrolysis. 
					Their work was greatly expanded upon by Michael Faraday in 1833. Current through a resistance causes localised heating, an effect James Prescott Joule studied mathematically in 1840.
					One of the most important discoveries relating to current was made accidentally by Hans Christian &Oslash;rsted in 1820, when, while preparing a lecture, 
					he witnessed the current in a wire disturbing the needle of a magnetic compass.
					He had discovered electromagnetism, a fundamental interaction between electricity and magnetics. 
					The level of electromagnetic emissions generated by electric arcing is high enough to produce electromagnetic interference, which can be detrimental to the workings of adjacent equipment.<br><br>
					In engineering or household applications, current is often described as being either direct current (DC) or alternating current (AC). 
					These terms refer to how the current varies in time. Direct current, as produced by example from a battery and required by most electronic devices, 
					is a unidirectional flow from the positive part of a circuit to the negative.
					If, as is most common, this flow is carried by electrons, they will be travelling in the opposite direction. 
					Alternating current is any current that reverses direction repeatedly; almost always this takes the form of a sine wave.
					Alternating current thus pulses back and forth within a conductor without the charge moving any net distance over time. 
					The time-averaged value of an alternating current is zero, but it delivers energy in first one direction, and then the reverse. 
					Alternating current is affected by electrical properties that are not observed under steady state direct current, such as inductance and capacitance.
					These properties however can become important when circuitry is subjected to transients, such as when first energised.<br><br>

