						<li>l = length of the conductors,</li>
						<li>A = cross-sectional area of conductor.</li>
					</ul><br><br>
		 			<h4>Ohm's Law :</h4>
					If the temperature and other conditions remain constant, the current through a conductor is proportional to the applied potential difference and it remains constant. Thus<br><br>
					<ul class="mainlists">
						<li>Current = Applied Voltage / Resistance of the circuit</li>
						<li>Resistance = Applied voltage / Current in the circuit</li>
						<li>Potential across resistance = Current x Resistance.</li>
					</ul><br><br>
					<h4>Conditions for Ohm's Law :</h4>
					<ul class="mainlists">
						<li>Ohm's law can be applied either to the entire circuit or a part of a circuit.</li>
						<li>When ohm's law is applied to a part circuit, part resistance and the potential across the part resistance should be used.</li>
						<li>The Ohm's law can be applied to DC as well as AC circuits. However, in case of AC circuits impedance Z, is used in place of resistance. Thus</li>
					</ul><br><br>
		 			<b>I = E / Z = Applied voltage / Impedance in the circuit</b><br><br>
		 			<h4>Conductance (G ) :</h4>
					Conductance is the reciprocal of (R) and is measure of the ease with which the current will flow through a substance. Thus<br>
					<b>G= 1 / R</b><br>
					The unit of conductance is mho.<br><br>
					<h4>ELECTRICAL POWER :</h4>
						Electrical power is expressed in terms of watts (W) and is given by<br>
						<b>W= E x I = I2 x R = E2 / R</b><br><br>
						Power is also expressed in terms of:<br>
						kW ( kilowatt ) ( =1000 W ) or
						MW ( megawatt ) which is 1000 kW or 1000,000 W.<br><br>
					<h4>Electrical Energy :</h4>
						Electrical energy is expressed in terms of kilowatt hours (kWh). Thus
						<b>1 kWh = 1 kW x 1 hour = 1000 watt-hours = 1000 x 60 x 60 watt-sec.</b><br><br>
					<h4>RESISTANCE COMBINATIONS :</h4>
						<b>Resistances in series: </b><br>
						<b>Resistances in series: </b><br>
						When resistances are connected in series, same current flows through all resistances, and overall resistance R, is given by<br><br>
						<b>R = R1 + R2 + R3</b><br><br>
